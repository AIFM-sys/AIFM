#ifndef _STRUCTS_H
#define _STRUCTS_H

/************************************************************************
*
*     Vertex    -  3D coordinate
*
*************************************************************************/

typedef struct {
	float x, y, z ;
} Vertex;

/************************************************************************
*
*     Color (R,G,B)
*
*************************************************************************/

typedef struct {
	float r, g, b ;
} Rgb;

/************************************************************************
*
*     Ray       -  3D coordinate
*
*************************************************************************/

typedef struct {
	float x, y, z;
} Ray;

#endif
